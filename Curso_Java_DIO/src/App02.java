public class App02 {
    public static void main(String[] args) {
        // Tipos primitivos inteiros:
        byte number = 1;    // -128 a 127 - 8 bits
        short number2 = 2;  // -32,768 a 32,767 - 16 bits
        int number3 = 3;    // -2,147,483,648 a 2,147,483,647 - 32 bits
        long number4 = 4;   // -9,223,372,036,854,775,808 a 9,223,372,036,854,775,807 - 64 bits

        // Tipos primitivos de ponto flutuante:
        float number5 = 5.0f;   // 32 bits
        double number6 = 6.0d;   // 64 bits

        // Tipo primitivo de caractere:
        var letter2 = "b";     // String - 16 bits por caractere - Unicode

        // Tipo primitivo booleano:
        boolean isTrue = true;  // 1 bit (representado como true ou false
    }
}
